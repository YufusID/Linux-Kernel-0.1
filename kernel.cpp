// Указываем компилятору не добавлять стандартные библиотеки, так как их нет
extern "C" void main() {
    // Указатель на начало видеопамяти для цветного текста
    char* video_memory = (char*)0xB8000;

    // Строка для вывода
    const char* str = "Welcome to My C++ OS!";

    // Цикл печати: каждый символ занимает 2 байта (код символа и цвет)
    for (int i = 0; str[i] != '\0'; ++i) {
        video_memory[i * 2] = str[i];     // Символ
        video_memory[i * 2 + 1] = 0x07;    // Цвет (серый на черном)
    }

    // Бесконечный цикл, чтобы процессор не выключился
    while(1);
}
